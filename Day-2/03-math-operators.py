#  In addition to sum, there are more mathematical operators in python:
#  +  : add
#  -  : subtract
#  *  : multiply
#  /  : divide
#  % (modulo) : return remainders from division
# ** : power raised

# Order of operations : PEMDAS (Parenthesis, Exponents Multiply, Divide, Add, Subtract)

print(3 + 2)  # 5
print(3 - 2)  # 1
print(3 * 2)  # 6
print(3 ** 2)  # 9
print(3 % 2)  # 1
