# Rounding off numbers
div = 8/3

# Displaying raw output
print(div)  # 2.6666666666666665

# Rounding off by converting into integer
print(int(div))  # 2

# Rounding off using round() function
print(round(div))  # 3

# Round off by Precision using round()
print(round(div, 4))  # 2.6667

# Using floor divison : //
print(8//3)  # 2

# multi-operators :
# += : increment and then assign
# -= : decrement and then assign
# *= : multiply and then assign
# /= : divide and then assign

score = 0
score += 1
print(score)  # 1
